package TasksPOO;

public class Calculation {
	double NumberOne;
	double NumberTwo;
	
	
	double Divisions (double NumberOne, double NumberTwo) {
		return NumberOne / NumberTwo;	
	}
	double Multiply (double NumberOne, double NumberTwo) {
		return NumberOne * NumberTwo;
	}
	double Subtraction (double NumberOne, double NumberTwo) {
		return NumberOne - NumberTwo;
	}
	double Sum (double  NumberOne, double NumberTwo) {
		return NumberOne + NumberTwo;
	}
	
}